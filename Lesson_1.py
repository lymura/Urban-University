#Строки и индексация строк

example = 'fjfjgkjmn4vnb5689'

print(example[0])   #выведете первый символ этой строки
print(example[-1])   #выведете последний символ этой строки
print(len(example))
print(example[8:])   #выведете вторую половину этой строки
print(example[::-1])    #выведете слово наоборот
print(example[::2])    #выведете каждый второй символ этой строки
